class Person {
  String name;
  String color;
  int age;


    

    Person(String uName, int uAge, String uColor) {

        name = uName;
      
        age = uAge;

       color = uColor;
    }

    String getName(){
      return name;
  
    }

    int getAge() {
      return age;

    }
 

    String getColor() {
      return color;
    }


    }
